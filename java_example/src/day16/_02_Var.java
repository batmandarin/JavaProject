package day16;

public class _02_Var {
public static void main(String[] args) {
	//Variable(변수) : 차후 사용되는 혹은 차후 변경도ㅓㅣㄹ 수 있는 값을 저장하는 공간
	//type(자료형)  : 변수(저장공간)의 형태, 공간을 생성할때만 봐준다
	
	//정수형 변수
	int var1 = 0;
	
	//실수형 변수
	double var2 = 0.0;
	
	//문자열형 변수
	String var3 = null;
	
	//불린형 변수
	boolean var4 = false;
	
	System.out.println(var1);
    System.out.println("합치기"+var2);
    System.out.println();
}
}
