package day01;

public class Opr1 {
	public static void main(String[] args) {
		//연산자 (자바에서 제공하는 기능) Operator 
		//+ , -, x , ÷ 등등을 연산자라고 부른다
		int num;
		//더하기 +
		String str;  //숫자의 더하기
	    str = "Hello" + "Java"; // 글자 더하기는 이렇게 해주시면 됩니다.
		//빼기 -
		num = 3+2;
		// 곱하기 *
		System.out.println();
		// 나누기는 /로
		num = 11-89;
        //나머지 구하기는 %로
		//출력 
		num = 234 * 345634;
		num =23/4;
		num = 122342%23;
		System.out.println(num);
		System.out.println(str);
		
	}
}
