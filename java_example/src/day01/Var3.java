package day01;

public class Var3 {
	public static void main(String[] args) {
		//변수로 이름, 생년월일, 키를 저장해놓고 사용
		String 문자열변수 = "신이현";
		String 문자열변수2 = "02.04.22";
		int 숫자형변수 = 182;
		System.out.println("이름 : "+문자열변수);
		System.out.println("생일 : "+문자열변수2);
		System.out.println("키 : "+숫자형변수+"cm");
		/*
		   이름 : 홍길동
		   생일 :  2000년 7월 15일
		   키 : 175.5cm
		   
		   단, 이름과 생년월일, 키는 변수로 저장해서 사용
		 */
		
	}
}
